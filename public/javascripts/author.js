function emptyCode(){
  $('#author_code').val('')
}

function originalNonroman(){

  $('#shownonroman').hide()
  $('#nonroman').show()
  if(! $('#author_traditional').value)
    $('#author_traditional').attr('value',
      $('#author_name').attr('value'))
}

function updateName(){
  $('#author_name').attr('value', romanize($('#author_original').attr('value')))
}

function romanize(string){
  var s = string
    .replace(/а/g, 'a')
    .replace(/б/g, 'b')
    .replace(/в/g, 'v')
    .replace(/г/g, 'g')
    .replace(/д/g, 'd')
    .replace(/е/g, 'e')
    .replace(/ё/g, 'ë')
    .replace(/ж/g, 'ž')
    .replace(/з/g, 'z')
    .replace(/и/g, 'i')
    .replace(/й/g, 'j')
    .replace(/к/g, 'k')
    .replace(/л/g, 'l')
    .replace(/м/g, 'm')
    .replace(/н/g, 'n')
    .replace(/о/g, 'o')
    .replace(/п/g, 'p')
    .replace(/р/g, 'r')
    .replace(/с/g, 's')
    .replace(/т/g, 't')
    .replace(/у/g, 'u')
    .replace(/ў/g, 'w')
    .replace(/ф/g, 'f')
    .replace(/х/g, 'h')
    .replace(/ц/g, 'c')
    .replace(/ч/g, 'č')
    .replace(/ш/g, 'š')
    .replace(/щ/g, 'šč')
    .replace(/ъ/g, '"')
    .replace(/ы/g, 'y')
    .replace(/ь/g, "'")
    .replace(/э/g, 'è')
    .replace(/ю/g, 'ju')
    .replace(/я/g, 'ja')
    .replace(/А/g, 'A')
    .replace(/Б/g, 'B')
    .replace(/В/g, 'V')
    .replace(/Г/g, 'G')
    .replace(/Д/g, 'D')
    .replace(/Е/g, 'E')
    .replace(/Є/g, 'Je')
    .replace(/Ё/g, 'Ë')
    .replace(/Ж/g, 'Ž')
    .replace(/З/g, 'Z')
    .replace(/И/g, 'I')
    .replace(/Й/g, 'J')
    .replace(/К/g, 'K')
    .replace(/Л/g, 'L')
    .replace(/М/g, 'M')
    .replace(/Н/g, 'N')
    .replace(/О/g, 'O')
    .replace(/П/g, 'P')
    .replace(/Р/g, 'R')
    .replace(/С/g, 'S')
    .replace(/Т/g, 'T')
    .replace(/У/g, 'U')
    .replace(/Ф/g, 'F')
    .replace(/Х/g, 'H')
    .replace(/Ц/g, 'C')
    .replace(/Ч/g, 'Č')
    .replace(/Ш/g, 'Š')
    .replace(/Щ/g, 'Šč')
    .replace(/Ъ/g, '"')
    .replace(/Ы/g, 'Y')
    .replace(/Ь/g, "'")
    .replace(/Э/g, 'È')
    .replace(/Ю/g, 'Ju')
    .replace(/Я/g, 'Ja')
  return s
}
